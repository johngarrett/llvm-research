#ifndef LLVM_LIB_TARGET_mRISCV_mRISCVSUBTARGET_H
#define LLVM_LIB_TARGET_mRISCV_mRISCVSUBTARGET_H

#include "mRISCVFrameLowering.h"
#include "mRISCVISelLowering.h"
#include "mRISCVInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "mRISCVGenSubtargetInfo.inc"

namespace llvm {
class mRISCVSubtarget : public mRISCVGenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  mRISCVInstrInfo InstrInfo;
  mRISCVFrameLowering FrameLowering;
  mRISCVTargetLowering TLInfo;
  mRISCVRegisterInfo RegInfo;

public:
  // Initializes the data members to match that of the specified triple.
  mRISCVSubtarget(const Triple &TT, StringRef CPU, StringRef FS,
                  const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  mRISCVSubtarget &initializeSubtargetDependencies(const Triple &TT,
                                                   StringRef CPU, StringRef FS,
                                                   const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const mRISCVInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const mRISCVRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const mRISCVTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
};
} // namespace llvm

#endif // end LLVM_LIB_TARGET_mRISCV_mRISCVSUBTARGET_H